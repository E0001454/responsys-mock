#!/usr/bin/env bash
set -euo pipefail

IMAGE_NAME="as-ms-ar-abc-frontend"
VERSION="${1:-latest}"
TAG="${IMAGE_NAME}:${VERSION}"
TAR_FILE="${IMAGE_NAME}-${VERSION}.tar"

