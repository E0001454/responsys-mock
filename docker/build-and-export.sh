    \
    --build-arg VITE_MS_MONITOR_LINEA_REAL="$(e VITE_MS_MONITOR_LINEA_REAL)" \
    --build-arg VITE_MS_MONITOR_LINEA_MOCK="$(e VITE_MS_MONITOR_LINEA_MOCK)" \
