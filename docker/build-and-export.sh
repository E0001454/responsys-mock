if [[ -f "$ENV_FILE" ]]; then
    echo "Loading variables from $ENV_FILE"
    set -a
    source <(grep -v '^\s*#' "$ENV_FILE" | grep -v '^\s*$')
    set +a
fi

e() { echo "${!1:-}"; }

echo ""
echo "Building image: $TAG"

