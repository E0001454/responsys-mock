    --build-arg VITE_MS_TAREAS_LINEA_MOCK="$(e VITE_MS_TAREAS_LINEA_MOCK)" \
    --build-arg VITE_MS_TAREAS_LINEA_USE_MOCK="$(e VITE_MS_TAREAS_LINEA_USE_MOCK)" \
