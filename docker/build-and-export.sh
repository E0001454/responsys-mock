    --build-arg VITE_MS_HORARIOS_LINEA_USE_MOCK="$(e VITE_MS_HORARIOS_LINEA_USE_MOCK)" \
    --build-arg VITE_MS_HORARIOS_CAMPANA_REAL="$(e VITE_MS_HORARIOS_CAMPANA_REAL)" \
